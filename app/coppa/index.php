                                <h1 class="display-3 mb-5">Children's Online Privacy Protection Act (COPPA)</h1>
                                <p>Websites that collect information from children under the age of thirteen are required to comply with the Federal Trade Commission (FTC) "Children's Online Privacy Protection Act" (COPPA).</p>
                                <p>The full text of the "Children's Online Privacy Protection Act" is below.</p>
                                <br />
                                <blockquote class="bg-warning p-5">
                                    <p align="center"><b>Children's<br> Online Privacy Protection Act of 1998 </b></p>
                                    <p align="center">
                                        <b>TITLE XIII-CHILDREN'S ONLINE PRIVACY PROTECTION</b></p>
                                    <p><b>SEC. 1301. SHORT TITLE.</b></p>
                                    <p>This title may be cited as the "Children's Online Privacy Protection Act of
                                        <br> 1998". </p>
                                    <p><b>SEC. 1302. DEFINITIONS.</b> </p>
                                    <p>In this title:</p>
                                    <p>(1) CHILD.—The term "child" means an individual under the age of 13.</p>
                                    <p>(2) OPERATOR.—The term "operator"—</p>
                                    <dir>
                                        <p></p>
                                        <p>(A) means any person who operates a website located on the Internet or an online
                                            <br> service and who collects or maintains personal information from or about the users of or
                                            <br> visitors to such website or online service, or on whose behalf such information is
                                            <br> collected or maintained, where such website or online service is operated for commercial
                                            <br> purposes, including any person offering products or services for sale through that website
                                            <br> or online service, involving commerce—</p>
                                        <dir>
                                            <p></p>
                                            <p>(i) among the several States or with 1 or more foreign nations;</p>
                                            <p>(ii) in any territory of the United States or in the District of Columbia, or between
                                                <br> any such territory and—</p>
                                            <dir>
                                                <p></p>
                                                <p>(I) another such territory; or</p>
                                                <p>(II) any State or foreign nation; or</p>
                                                <p> </p>
                                            </dir>
                                            <p></p>
                                            <p>(iii) between the District of Columbia and any State, territory, or foreign nation; but</p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(B) does not include any nonprofit entity that would otherwise be exempt from coverage
                                            <br> under section 5 of the Federal Trade Commission Act (15 U.S.C. 45).</p>
                                        <p> </p>
                                    </dir>
                                    <p></p>
                                    <p>(3) COMMISSION.—The term "Commission" means the Federal Trade
                                        <br> Commission.
                                    </p>
                                    <p>(4) DISCLOSURE.—The term "disclosure" means, with respect to personal
                                        <br> information—
                                    </p>
                                    <dir>
                                        <p></p>
                                        <p>(A) the release of personal information collected from a child in identifiable form by
                                            <br> an operator for any purpose, except where such information is provided to a person other
                                            <br> than the operator who provides support for the internal operations of the website and does
                                            <br> not disclose or use that information for any other purpose; and</p>
                                        <p>(B) making personal information collected from a child by a website or online service
                                            <br> directed to children or with actual knowledge that such information was collected from a
                                            <br> child, publicly available in identifiable form, by any means including by a public
                                            <br> posting, through the Internet, or through—</p>
                                        <dir>
                                            <p></p>
                                            <p>(i) a home page of a website;</p>
                                            <p>(ii) a pen pal service;</p>
                                            <p>(iii) an electronic mail service;</p>
                                            <p>(iv) a message board; or</p>
                                            <p>(v) a chat room.</p>
                                            <p> </p>
                                        </dir>
                                        <br>
                                    </dir>
                                    <p></p>
                                    <p>(5) FEDERAL AGENCY.—The term "Federal agency" means an agency, as that
                                        <br> term is defined in section 551(1) of title 5, United States Code.</p>
                                    <p>(6) INTERNET.—The term "Internet" means collectively the myriad of
                                        <br> computer and telecommunications facilities, including equipment and operating software,
                                        <br> which comprise the interconnected world-wide network of networks that employ the
                                        <br> Transmission Control Protocol/ Internet Protocol, or any predecessor or successor
                                        <br> protocols to such protocol, to communicate information of all kinds by wire or radio.</p>
                                    <p>(7) PARENT.—The term "parent" includes a legal guardian.</p>
                                    <p>(8) PERSONAL INFORMATION.—The term "personal information" means
                                        <br> individually identifiable information about an individual collected online,
                                        <br> including—
                                    </p>
                                    <dir>
                                        <p></p>
                                        <p>(A) a first and last name;</p>
                                        <p>(B) a home or other physical address including street name and name of a city or town;</p>
                                        <p>(C) an e-mail address;</p>
                                        <p>(D) a telephone number;</p>
                                        <p>(E) a Social Security number;</p>
                                        <p>(F) any other identifier that the Commission determines permits the physical or online
                                            <br> contacting of a specific individual; or</p>
                                        <p>(G) information concerning the child or the parents of that child that the website
                                            <br> collects online from the child and combines with an identifier described in this
                                            <br> paragraph.
                                        </p>
                                        <p> </p>
                                    </dir>
                                    <p></p>
                                    <p>(9) VERIFIABLE PARENTAL CONSENT.—The term "verifiable parental consent"
                                        <br> means any reasonable effort (taking into consideration available technology), including a
                                        <br> request for authorization for future collection, use, and disclosure described in the
                                        <br> notice, to ensure that a parent of a child receives notice of the operator's personal
                                        <br> information collection, use, and disclosure practices, and authorizes the collection, use,
                                        <br> and disclosure, as applicable, of personal information and the subsequent use of that
                                        <br> information before that information is collected from that child.</p>
                                    <p>(10) WEBSITE OR ONLINE SERVICE DIRECTED TO CHILDREN.—</p>
                                    <dir>
                                        <p></p>
                                        <p>(A) IN GENERAL.—The term "website or online service directed to
                                            <br> children" means—</p>
                                        <dir>
                                            <p></p>
                                            <p>(i) a commercial website or online service that is targeted to children; or</p>
                                            <p>(ii) that portion of a commercial website or online service that is targeted to
                                                <br> children.
                                            </p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(B) LIMITATION.—A commercial website or online service, or a portion of a
                                            <br> commercial website or online service, shall not be deemed directed to children solely for
                                            <br> referring or linking to a commercial website or online service directed to children by
                                            <br> using information location tools, including a directory, index, reference, pointer, or
                                            <br> hypertext link.</p>
                                        <p> </p>
                                    </dir>
                                    <p></p>
                                    <p>(11) PERSON.—The term "person" means any individual, partnership,
                                        <br> corporation, trust, estate, cooperative, association, or other entity.</p>
                                    <p>(12) ONLINE CONTACT INFORMATION.—The term "online contact information"
                                        <br> means an e-mail address or an-other substantially similar identifier that permits direct
                                        <br> contact with a person online.</p>
                                    <p><b>SEC. 1303. REGULATION OF UNFAIR AND DECEPTIVE ACTS AND PRACTICES IN CONNECTION WITH<br>
                                                            THE COLLECTION AND USE OF PERSONAL INFORMATION FROM AND ABOUT CHILDREN ON THE INTERNET.</b></p>
                                    <dir>
                                        <p></p>
                                        <p>(a) ACTS PROHIBITED.—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) IN GENERAL.—It is unlawful for an operator of a website or online service
                                                <br> directed to children, or any operator that has actual knowledge that it is collecting
                                                <br> personal information from a child, to collect personal information from a child in a
                                                <br> manner that violates the regulations prescribed under subsection (b).</p>
                                            <p>(2) DISCLOSURE TO PARENT PROTECTED.—Notwithstanding paragraph (1), neither an
                                                <br> operator of such a website or online service nor the operator's agent shall be held to be
                                                <br> liable under any Federal or State law for any disclosure made in good faith and following
                                                <br> reasonable procedures in responding to a request for disclosure of per-sonal information
                                                <br> under subsection (b)(1)(B)(iii) to the parent of a child.</p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(b) REGULATIONS.—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) IN GENERAL.—Not later than 1 year after the date of the enactment of this Act,
                                                <br> the Commission shall promulgate under section 553 of title 5, United States Code,
                                                <br> regulations that—</p>
                                            <dir>
                                                <p></p>
                                                <p>(A) require the operator of any website or online service directed to children that
                                                    <br> collects personal information from children or the operator of a website or online service
                                                    <br> that has actual knowledge that it is collecting personal information from a child—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) to provide notice on the website of what information is collected from children by
                                                        <br> the operator, how the operator uses such information, and the operator's disclosure
                                                        <br> practices for such information; and</p>
                                                    <p>(ii) to obtain verifiable parental consent for the collection, use, or disclosure of
                                                        <br> personal information from children;</p>
                                                    <p> </p>
                                                </dir>
                                                <p></p>
                                                <p>(B) require the operator to provide, upon request of a parent under this subparagraph
                                                    <br> whose child has provided personal information to that website or online service, upon
                                                    <br> proper identification of that parent, to such par-ent—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) a description of the specific types of personal information collected from the
                                                        <br> child by that operator;</p>
                                                    <p>(ii) the opportunity at any time to refuse to permit the operator's further use or
                                                        <br> maintenance in retrievable form, or future online collection, of personal information from
                                                        <br> that child; and</p>
                                                    <p>(iii) notwithstanding any other provision of law, a means that is reasonable under the
                                                        <br> circumstances for the parent to obtain any personal information collected from that child;</p>
                                                    <p> </p>
                                                </dir>
                                                <p></p>
                                                <p>(C) prohibit conditioning a child's participation in a game, the offering of a prize,
                                                    <br> or another activity on the child disclosing more personal information than is reasonably
                                                    <br> necessary to participate in such activity; and</p>
                                                <p>(D) require the operator of such a website or online service to establish and maintain
                                                    <br> reasonable procedures to protect the confidentiality, security, and integrity of personal
                                                    <br> information collected from children.</p>
                                                <p> </p>
                                            </dir>
                                            <p></p>
                                            <p>(2) WHEN CONSENT NOT REQUIRED.—The regulations shall provide that verifiable
                                                <br> parental consent under paragraph (1)(A)(ii) is not required in the case of—</p>
                                            <dir>
                                                <p></p>
                                                <p>(A) online contact information collected from a child that is used only to respond
                                                    <br> directly on a one-time basis to a specific request from the child and is not used to
                                                    <br> recontact the child and is not maintained in retrievable form by the operator;</p>
                                                <p>(B) a request for the name or online contact information of a parent or child that is
                                                    <br> used for the sole purpose of obtaining parental consent or providing notice under this
                                                    <br> section and where such information is not maintained in retrievable form by the operator
                                                    <br> if parental consent is not obtained after a reasonable time;</p>
                                                <p>(C) online contact information collected from a child that is used only to respond more
                                                    <br> than once directly to a specific request from the child and is not used to recontact the
                                                    <br> child beyond the scope of that request—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) if, before any additional response after the initial response to the child, the
                                                        <br> operator uses reasonable efforts to provide a parent notice of the online contact
                                                        <br> information collected from the child, the purposes for which it is to be used, and an
                                                        <br> opportunity for the parent to request that the operator make no further use of the
                                                        <br> information and that it not be maintained in retrievable form; or</p>
                                                    <p>(ii) without notice to the parent in such circumstances as the Commission may determine
                                                        <br> are appropriate, taking into consideration the benefits to the child of access to
                                                        <br> information and services, and risks to the security and privacy of the child, in
                                                        <br> regulations promulgated under this subsection;</p>
                                                    <p> </p>
                                                </dir>
                                                <p></p>
                                                <p>(D) the name of the child and online contact information (to the extent reasonably
                                                    <br> necessary to protect the safety of a child participant on the site)—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) used only for the purpose of protecting such safety;</p>
                                                    <p>(ii) not used to recontact the child or for any other purpose; and</p>
                                                    <p>(iii) not disclosed on the site, if the operator uses reasonable efforts to provide a
                                                        <br> parent notice of the name and online contact information collected from the child, the
                                                        <br> purposes for which it is to be used, and an opportunity for the parent to request that the
                                                        <br> operator make no further use of the information and that it not be maintained in
                                                        <br> retrievable form; or</p>
                                                    <p> </p>
                                                </dir>
                                                <p></p>
                                                <p>(E) the collection, use, or dissemination of such information by the operator of such a
                                                    <br> website or online service necessary—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) to protect the security or integrity of its website;</p>
                                                    <p>(ii) to take precautions against liability;</p>
                                                    <p>(iii) to respond to judicial process; or</p>
                                                    <p>(iv) to the extent permitted under other provisions of law, to provide information to
                                                        <br> law enforcement agencies or for an investigation on a matter related to public safety.
                                                        <br> 1815
                                                    </p>
                                                    <p> </p>
                                                </dir>
                                                <br>
                                            </dir>
                                            <p></p>
                                            <p>(3) TERMINATION OF SERVICE.—The regulations shall permit the operator of a website
                                                <br> or an online service to terminate service provided to a child whose parent has refused,
                                                <br> under the regulations prescribed under paragraph (1)(B)(ii), to permit the operator's
                                                <br> further use or maintenance in retrievable form, or future online collection, of personal
                                                <br> information from that child.</p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(c) ENFORCEMENT.—Subject to sections 1304 and 1306, a violation of a regulation
                                            <br> prescribed under subsection (a) shall be treated as a violation of a rule defining an
                                            <br> unfair or deceptive act or practice prescribed under section 18(a)(1)(B) of the Federal
                                            <br> Trade Commission Act (15 U.S.C. 57a(a)(1)(B)).</p>
                                        <p>(d) INCONSISTENT STATE LAW.—No State or local government may impose any liability
                                            <br> for commercial activities or actions by operators in interstate or foreign commerce in
                                            <br> connection with an activity or action described in this title that is inconsistent with
                                            <br> the treatment of those activities or actions under this section.</p>
                                        <p> </p>
                                    </dir>
                                    <br>
                                    <b>
                                                                                        </b>
                                    <p><b>SEC. 1304. SAFE HARBORS.</b></p>
                                    <dir>
                                        <p></p>
                                        <p>(a) GUIDELINES.—An operator may satisfy the requirements of regulations issued
                                            <br> under section 1303(b) by following a set of self-regulatory guidelines, issued by
                                            <br> representatives of the marketing or online industries, or by other persons, approved under
                                            <br> subsection (b).</p>
                                        <p>(b) INCENTIVES.—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) SELF-REGULATORY INCENTIVES.—In prescribing regulations under section 1303, the
                                                <br> Commission shall provide incentives for self-regulation by operators to implement the
                                                <br> protections afforded children under the regulatory requirements described in subsection
                                                <br> (b) of that section.</p>
                                            <p>(2) DEEMED COMPLIANCE.—Such incentives shall include provisions for ensuring that
                                                <br> a person will be deemed to be in compliance with the requirements of the regulations under
                                                <br> section 1303 if that person complies with guidelines that, after notice and comment, are
                                                <br> approved by the Commission upon making a determination that the guidelines meet the
                                                <br> requirements of the regulations issued under section 1303.</p>
                                            <p>(3) EXPEDITED RESPONSE TO REQUESTS.—The Commission shall act upon requests for
                                                <br> safe harbor treatment within 180 days of the filing of the request, and shall set forth in
                                                <br> writing its conclusions with regard to such requests.</p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(c) APPEALS.—Final action by the Commission on a request for approval of
                                            <br> guidelines, or the failure to act within 180 days on a request for approval of guidelines,
                                            <br> submitted under subsection (b) may be appealed to a district court of the United States of
                                            <br> appropriate jurisdiction as provided for in section 706 of title 5, United States Code.</p>
                                        <p> </p>
                                    </dir>
                                    <br>
                                    <b>
                                                                                        </b>
                                    <p><b>SEC. 1305. ACTIONS BY STATES.</b></p>
                                    <dir>
                                        <p></p>
                                        <p>(a) IN GENERAL.—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) CIVIL ACTIONS.—In any case in which the attorney general of a State has reason
                                                <br> to believe that an interest of the residents of that State has been or is threatened or
                                                <br> adversely affected by the engagement of any person in a practice that violates any
                                                <br> regulation of the Commission prescribed under section 1303(b), the State, as parens
                                                <br> patriae, may bring a civil action on behalf of the residents of the State in a district
                                                <br> court of the United States of appropriate jurisdiction to—</p>
                                            <dir>
                                                <p></p>
                                                <p>(A) enjoin that practice;</p>
                                                <p>(B) enforce compliance with the regulation;</p>
                                                <p>(C) obtain damage, restitution, or other compensation on behalf of residents of the
                                                    <br> State; or</p>
                                                <p>(D) obtain such other relief as the court may consider to be appropriate.</p>
                                                <p> </p>
                                            </dir>
                                            <p></p>
                                            <p>(2) NOTICE.—</p>
                                            <dir>
                                                <p></p>
                                                <p>(A) IN GENERAL.—Before filing an action under paragraph (1), the attorney general
                                                    <br> of the State involved shall provide to the Commission—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) written notice of that action; and</p>
                                                    <p>(ii) a copy of the complaint for that action.</p>
                                                    <p> </p>
                                                </dir>
                                                <p></p>
                                                <p>(B) EXEMPTION.—</p>
                                                <dir>
                                                    <p></p>
                                                    <p>(i) IN GENERAL.—Subparagraph (A) shall not apply with respect to the filing of an
                                                        <br> action by an attorney general of a State under this subsection, if the attorney general
                                                        <br> determines that it is not feasible to provide the notice described in that subparagraph
                                                        <br> before the filing of the action.</p>
                                                    <p>(ii) NOTIFICATION.—In an action described in clause (i), the attorney general of a
                                                        <br> State shall provide notice and a copy of the complaint to the Commission at the same time
                                                        <br> as the attorney general files the action.</p>
                                                    <p> </p>
                                                </dir>
                                                <br>
                                            </dir>
                                            <br>
                                        </dir>
                                        <p></p>
                                        <p>(b) INTERVENTION.—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) IN GENERAL.—On receiving notice under subsection (a)(2), the Commission shall
                                                <br> have the right to intervene in the action that is the subject of the notice.</p>
                                            <p>(2) EFFECT OF INTERVENTION.—If the Commission intervenes in an action under
                                                <br> subsection (a), it shall have the right—</p>
                                            <dir>
                                                <p></p>
                                                <p>(A) to be heard with respect to any matter that arises in that action; and</p>
                                                <p>(B) to file a petition for appeal.</p>
                                                <p> </p>
                                            </dir>
                                            <p></p>
                                            <p>(3) AMICUS CURIAE.—Upon application to the court, a person whose self-regulatory
                                                <br> guidelines have been approved by the Commission and are relied upon as a defense by any
                                                <br> defendant to a proceeding under this section may file amicus curiae in that proceeding.</p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(c) CONSTRUCTION.—For purposes of bringing any civil action under subsection (a),
                                            <br> nothing in this title shall be construed to prevent an attorney general of a State from
                                            <br> exercising the powers conferred on the attorney general by the laws of that State to—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) conduct investigations;</p>
                                            <p>(2) administer oaths or affirmations; or</p>
                                            <p>(3) compel the attendance of witnesses or the production of documentary and other
                                                <br> evidence.
                                            </p>
                                            <p> </p>
                                        </dir>
                                        <p></p>
                                        <p>(d) ACTIONS BY THE COMMISSION.—In any case in which an action is instituted by or
                                            <br> on behalf of the Commission for violation of any regulation prescribed under section 1303,
                                            <br> no State may, during the pendency of that action, institute an action under subsection (a)
                                            <br> against any defendant named in the complaint in that action for violation of that
                                            <br> regulation.
                                        </p>
                                        <p>(e) VENUE; SERVICE OF PROCESS.—</p>
                                        <dir>
                                            <p></p>
                                            <p>(1) VENUE.—Any action brought under subsection (a) may be brought in the district
                                                <br> court of the United States that meets applicable requirements relating to venue under
                                                <br> section 1391 of title 28, United States Code.</p>
                                            <p>(2) SERVICE OF PROCESS.—In an action brought under subsection (a), process may be
                                                <br> served in any district in which the defendant—</p>
                                            <dir>
                                                <p></p>
                                                <p>(A) is an inhabitant; or</p>
                                                <p>(B) may be found.</p>
                                                <p> </p>
                                            </dir>
                                            <br>
                                        </dir>
                                        <br>
                                    </dir>
                                    <p></p>
                                    <p><b>SEC. 1306. ADMINISTRATION AND APPLICABILITY OF ACT.</b></p>
                                    <dir>
                                        <p></p>
                                        <p>(a) IN GENERAL.—Except as otherwise provided, this title shall be enforced by the
                                            <br> Commission under the Federal Trade Commission Act (15 U.S.C. 41 et seq.).</p>
                                        <p>(b) PROVISIONS.—Compliance with the requirements imposed under this title shall be
                                            <br> enforced under—(1) section 8 of the Federal Deposit Insurance Act (12 U.S.C. 1818),
                                            <br> in the case of—</p>
                                        <dir>
